import { toast } from "react-toastify";
export const successToaster = (message) =>
  toast.success(message, {
    position: "top-right",
    autoClose: 4000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    color: "#e3e3e3",
    theme: "colored",
  });
